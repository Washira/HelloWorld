'''
Input: รับสตริงที่มีแต่เลข 0 กับ 1
Process: แปลงสตริงเลขฐานสองที่ได้รับให้เป็นจำนวนเต็มในฐานสิบ (คือ int นั่นเอง) ตัวอย่างเช่น "01101" = 0×24 + 1×23 + 1×22 + 0×21 + 1×20 = 13
Output: จำนวนเต็มที่ได้
'''
a = [int(e) for e in input().strip()]
b = len(a)-1
n = 0
for i in a:
  n += i*(2**b)
  b -=1
print(n)
